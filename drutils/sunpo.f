      SUBROUTINE sunpo(MJD,UT,PI,RASUN,DECSUN)
C
C     THIS ROUTINE CALCULATES THE SUN'S RA AND DEC ON JULIAN DATE MJD
C     at time UT.
C
      implicit none
      integer mjd
      real*8 PI,UT
      real*8 RASUN,DECSUN,conv,tcen,slam,oblq
C
      CONV = PI/180.0
      TCEN = (MJD+2440000.D0-2415020.D0+UT/86400.d0)/36525.D0
      SLAM = DMOD(279.697D0+36000.76892*TCEN,360.D0)*CONV
      OBLQ = (23.452-0.01301*TCEN)*CONV
      DECSUN = ASIN(SIN(SLAM)*SIN(OBLQ))
      RASUN = ATAN2(SIN(SLAM)*COS(OBLQ),COS(SLAM))
      IF (RASUN.LT.0) RASUN = RASUN + 2*PI
      RETURN
      END
